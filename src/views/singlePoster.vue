<template>
  <div>
    <h1>Single Poster View</h1>
      <img :src="require(`../assets/char-${poster.id}.png`)" alt="Poster" />
      <h1>{{ poster.job }}</h1>

  </div>
</template>

<script>
export default {
  computed: {
    posters() {
      return this.$store.state.Posters;
    },
    poster() {
      return this.posters.find((posters) => posters.id === this.$route.params.id);
    },
  },
};
</script>

